    private void btn_ResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ResetActionPerformed
        // Database connection string
        String connectionString = "jdbc:sqlserver://DESKTOP-3RD2OGB;Database=OOP_CW;integratedSecurity=true;TrustServerCertificate=true;";

        Connection con = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;

        try {
            // Connect to the database
            con = DriverManager.getConnection(connectionString);

            // SQL statement to check if the lecturerID and secretPin are correct
            String sql = "SELECT * FROM Lecturer WHERE lecturerID = ? AND secretPin = ?";
            stmt = con.prepareStatement(sql);

            // Set parameters
            stmt.setString(1, txt_lecturerUsername.getText());
            stmt.setString(2, txt_lecturerSecretPin.getText());

            // Execute query
            rs = stmt.executeQuery();

            // If the lecturer is found, check if the new password and confirm password match
            if (rs.next()) {
                String newPassword = txt_studentId2.getText(); // New Password
                String confirmPassword = txt_studentId3.getText(); // Confirm Password

                if (newPassword.equals(confirmPassword)) {
                    // Update the password in the database
                    sql = "UPDATE Lecturer SET lecturePassword = ? WHERE lecturerID = ?";
                    stmt = con.prepareStatement(sql);
                    stmt.setString(1, newPassword);
                    stmt.setString(2, txt_lecturerUsername.getText());

                    int rowsUpdated = stmt.executeUpdate();

                    if (rowsUpdated > 0) {
                        JOptionPane.showMessageDialog(this, "Password has been reset successfully.");
                        // Optionally, redirect to login page
                        LecturerLogin lecturerLogin = new LecturerLogin();
                        lecturerLogin.setVisible(true);
                        this.setVisible(false);
                    } else {
                        JOptionPane.showMessageDialog(this, "Error: Password could not be updated.");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "New Password and Confirm Password do not match.");
                }
            } else {
                // Invalid lecturerID or secretPin
                JOptionPane.showMessageDialog(this, "Invalid Lecturer ID or Secret Pin.");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        } finally {
            // Ensure resources are closed
            try {
                if (rs != null) {
                    rs.close();
                }
                if (stmt != null) {
                    stmt.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error closing resources: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btn_ResetActionPerformed

